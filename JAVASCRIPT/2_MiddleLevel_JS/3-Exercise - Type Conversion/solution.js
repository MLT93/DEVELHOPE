// Create a variable and set it as true
let myVariable = true;

// Print the value and data type
console.log(myVariable); // Output: true
console.log(typeof myVariable); // Output: boolean

// Convert it to a numeric data type
let numericValue = Number(myVariable);

// Print the value and data type
console.log(numericValue); // Output: 1
console.log(typeof numericValue); // Output: number

// Convert it to string data type
let stringValue = String(myVariable);

// Print the value and data type
console.log(stringValue); // Output: "true"
console.log(typeof stringValue); // Output: string

// Convert it back to a boolean data type
let booleanValue = Boolean(stringValue);

// Print the value and data type
console.log(booleanValue); // Output: true
console.log(typeof booleanValue); // Output: boolean
